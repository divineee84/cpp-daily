#include <iostream>

int main() {
    int age = 18;             // integer
    double pi = 3.14159;      // decimal
    char grade = 'A';         // character

    std::cout << "Age: " << age << '\n';
    std::cout << "Pi: " << pi << '\n';
    std::cout << "Grade: " << grade << '\n';

    return 0;
}
